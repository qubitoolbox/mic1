    private String StringBuilder(String[] str_data, int index) {
        String return_str = "";
        int i;
        for (i = index; i < str_data.length; i++) {
            return_str += str_data[i];
            if ((i != str_data.length - 1)) {
                return_str += ":";
            }
        }
        return return_str;
    }

